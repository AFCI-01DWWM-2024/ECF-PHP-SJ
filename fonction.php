<?php

function Addition($nombre1, $nombre2) {
    return $nombre1 + $nombre2;
}

function Soustraction($nombre1, $nombre2) {
    return $nombre1 - $nombre2;
}

function Multiplication($nombre1, $nombre2) {
    return $nombre1 * $nombre2;
}

function Division($nombre1, $nombre2) {
    return $nombre1 / $nombre2;
}

